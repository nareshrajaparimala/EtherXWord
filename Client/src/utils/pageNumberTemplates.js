// Helper function to format page number based on format type
export const formatPageNumber = (pageNum, format) => {
    switch (format) {
        case 'roman-upper':
            return toRoman(pageNum);
        case 'roman-lower':
            return toRoman(pageNum).toLowerCase();
        case 'alpha-upper':
            return String.fromCharCode(64 + pageNum); // A, B, C...
        case 'alpha-lower':
            return String.fromCharCode(96 + pageNum); // a, b, c...
        case 'arabic':
        default:
            return pageNum.toString();
    }
};

// Roman numeral conversion
const toRoman = (num) => {
    const values = [1000, 900, 500, 400, 100, 90, 50, 40, 10, 9, 5, 4, 1];
    const symbols = ['M', 'CM', 'D', 'CD', 'C', 'XC', 'L', 'XL', 'X', 'IX', 'V', 'IV', 'I'];
    let result = '';
    for (let i = 0; i < values.length; i++) {
        while (num >= values[i]) {
            result += symbols[i];
            num -= values[i];
        }
    }
    return result;
};
